#include <iostream>
#include <time.h>
using namespace std;

int main()
{
    //基于当前系统的当前日期/时间
    time_t now = time(0);

    //把now转换成字符串形式
    char* dt = ctime(&now);

    cout << "本地日期和时间：" << dt << endl;

    //把now转换为tm结构
    tm *gmtm = gmtime(&now);
    dt = asctime(gmtm);
    cout << "UTC日期和时间：" << dt << endl;
}